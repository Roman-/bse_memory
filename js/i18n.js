// returns the word \param w translated to language specified in Glob.currentLocale
function tr(w) {
    if (!Glob.localeMap.hasOwnProperty(w)) {
        console.warn("no translation for " + w);
        return w;
    }
    // in case we haven't selected lang yet but want to init menu
    let lang = Glob.currentLocale;
    if (lang == '')
        lang = 'en';
    var options = Glob.localeMap[w];
    if (options.hasOwnProperty(lang))
        return options[lang];
    // else consider that the word is in English
    return w;
}
